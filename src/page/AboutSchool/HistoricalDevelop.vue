	<p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px"></span>电气与计算机工程学院创立于2006年9月，创办之初以“信息科学与技术系”命名，系下设计算机科学与技术专业，常会友教授担任系主任。2007年，伍小明副教授担任系主任，同年10月系名更改为“计算机工程系”。</p> 
