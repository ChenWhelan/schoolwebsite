      <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align:left">
        <span style="font-family: , 'microsoft yahei'; font-size: 18px"
          >3、开展学科竞赛</span
        >
      </p>
      <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align:left">
        <span style="font-family: , 'microsoft yahei'; font-size: 18px"
