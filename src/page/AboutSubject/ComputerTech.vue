        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >学科基础：计算机科学导论、高级语言程序设计、操作系统原理、数据结构与算法、面向对象程序设计、计算机组成原理、数据库原理、软件工程、计算机网络。</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >专业类课程：数字图像处理及应用、编译原理、计算机系统结构、Android应用开发、Python程序设计、WEB编程技术、系统分析与设计、大数据与云计算等。</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span
