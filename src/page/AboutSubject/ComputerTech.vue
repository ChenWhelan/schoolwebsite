        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >计算机科学与技术专业面向粤港澳大湾区及地方经济建设、社会发展对计算机应用型人才的需求，以“立德树人”为根本，以“宽口径、厚基础、强能力、以就业为导向”，培养具有良好科学素养、掌握计算机科学基础理论知识、以及工程技术的基本方法与技能、具有较强的工程实践能力和创新意识的计算机类应用创新型人才。</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <strong
          ><span style="font-size: 18px; font-family: , 'microsoft yahei'"
            >2、培养目标</span
          ></strong
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >计算机科学与技术专业聚焦工程教育专业认证，结合产教融合、学科竞赛、实践及科研项目，从知识、能力、素质三个维度，以“成果导向”培养应用型卓越工程师。</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >在应用型人才培养基础上，本专业注重学生素质和能力的提升、注重学生个性化培养，提出了PIQE–CDIO（PIQE:
          Professional, Innovative and Qualitative
          Engineers)的教育模式，学生毕业后3-5年，达到以下培养目标：</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >（1）道德修养：具有良好的科学和工程应用及开发素养，具有良好的职业道德，在工作中表现出良好的社会责任感、事业心，并具有安全与环保意识；</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >（2）工程能力：掌握良好的专业基础知识和专业技能，能为计算机复杂工程项目提供系统性解决方案，具有工程实践能力，能承担系统的开发、测试、维护等任务；</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >（3）工程伦理：在工程实践中能够综合考虑社会、健康、安全、法律、经济、文化以及环境等因素；</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >（4）团队合作：具有良好的团队合作精神、交流与沟通能力，能够组织、协调团队成员，能在团队中发挥有效作用；</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >（5）终身学习：具有国际视野，能自觉跟踪计算机及相关领域的前沿知识及技术，能积极主动继续学习以适应不断发展的需求。</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <strong
          ><span style="font-size: 18px; font-family: , 'microsoft yahei'"
            >3、培养规格</span
          ></strong
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
        <span style="font-size: 18px; font-family: , 'microsoft yahei'"
          >学校明确本专业的学制为4年。按照学分制管理机制，实行弹性学习年限，但不得低于3年或超过7年。最低毕业总学分为156+（8~10）（8~10学分是学生成长教育课程学分）。要求学生能够将数学、自然科学、计算机领域的基础理论知识、以及工程应用的方法及技术用于解决本专业领域的复杂工程问题，通过了解计算机专业领域的知识背景，能够运用数学、自然科学、数据结构及算法、软件开发、软件工程等基本原理及方法，经过查阅文献资料、分析、表达，提出解决计算机软硬件系统中复杂工程问题的方法等。</span
        >
      </p>
<p style="text-indent: 43px; line-height: 37px;text-align: left;">
