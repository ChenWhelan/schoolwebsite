          >工程实践与毕业设计：高级语言程序设计实践、操作系统课程设计、数据结构与算法课程设计、程序设计实训、计算机组成原理课程设计、软件工程课程设计、MySQL数据库应用实践、计算机网络实训、嵌入式系统实践、系统开发实训、移动应用开发实训、企业项目实践、认识与工作实习、毕业设计等。</span
        >
      </p>
