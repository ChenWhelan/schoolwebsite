						<article class="article">
							<p style="line-height:180%;text-indent:2em;font-size:14pt;text-align:center"><span style="font-size:24px"><strong>电子与移动通信工程系</strong></span></p> 
<p style="line-height:180%;text-indent:2em;font-size:14pt;text-align:center"><span style="font-size:18px">(Department of Communication and Electronic Engineering)</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本系遵循广州南方学院“厚基础、宽口径、重实践、强能力”的教学指导思想，坚持“紧贴信息技术与市场需求、高起点、有特色、更开放”的办学理念，结合国内尤其珠三角经济社会发展的特点，以IT市场人才需求为导向进行课程设置和师资队伍建设。电子与移动通信工程系现已开设电子信息科学与技术、通信工程两个专业，培养可服务于广东省珠三角企事业单位、IT技术开发公司、通信运营商、电子信息行业、教学科研机构等的应用型电子通信工程师。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本专业教师理论扎实、经验丰富，其中多一半教师具有企业工程经历。拥有多名教授、博士教师，整体年龄、学历及职称结构合理，为培养面向未来的应用型人才提供坚实保证。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本系采取专业基础教育与专业教育、优势学科与分类培养相结合的方式，逐步形成了在办学条件、专业与课程建设、师资队伍建设、实验室与实训实习基地建设、学生管理等方面的人才培养特色。构建了“基础实验、综合实验、综合实训、企业实习与实训”为一体的实践平台，较好地将学生的学习兴趣、就业市场方向融入到人才培养方案中，做到了学生培养与企业就业的良好接轨。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">具体特色为：</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">1、构建卓越工程师人才培养体系</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">以培养应用型卓越工程师为目标，每年选拔优秀的学生组成电子通信卓越工程师班，实行导师制，以项目驱动的形式指导学生参与科研项目，提高实践动手能力，使学生在专业技能上更具核心竞争力。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">2、电子通信特色学科建设</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">为使毕业生得到社会、国际认可，本系重点开展了电子通信特色学科建设工作，与企业和国际大学合作，强调学科课程体系与社会需求高度接轨、学历教育与职业技能培训兼修、理论结合实际、创新与实践能力并重，取得了较好的成绩。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">3、多维度培养学生实践能力</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本系注重与行业深度融合，积累了多年学生卓越实践能力和应用能力的培养经验，在学院基础实验室和专业实验室的保障下，带领学生参与教师项目、企业项目、学科竞赛等，并为学生提供实训和企业项目导师，攻克实践难题，每年均有大批学生获得国家和省级奖励，得到了社会的广泛认可。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="text-indent:2em;font-size:20px"><br></span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="text-indent:2em;font-size:20px">一、电子信息科学与技术专业介绍</span></strong><br></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">1、专业定位</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">电子信息科学与技术专业主要培养适应21世纪电子信息产业科技与社会经济发展需要，理论联系实际、基础扎实、动手能力强，掌握电子信息系统的基础理论与分析设计方法，能在电子信息科学、电子工程、通信网络及它相关领域和企事业单位从事电子产品与项目设计开发，电子与通信工程应用、技术改造，电子与通信设备运行、维护和管理等方面的工作，具有市场竞争力和一定创新和实践能力的应用型高级技术人才。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">2、培养目标</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本专业以工程教育专业认证为标准，培养能在电子信息工程、通信工程等相关单位从事开发与设备运行等方面的应用型高级技术人才。可就职电子信息类研发行业以及企事业单位，从事电子产品开发、生产、经营与管理工作。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">3、培养规格</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">学校明确本专业的学制为4年。按照学分制管理机制，实行弹性学习年限，但不得低于3年或超过7年。最低毕业总学分为156+（8~10）（8~10学分是学生成长教育课程学分）。要求学生能够将数学、自然科学、电子信息等工程基础的理论与方法用于解决本专业领域的复杂工程问题，能够应用数学、自然科学、工程科学的基本原理，经过识别、表达，通过文献等资料的研究，分析电子信息专业领域的复杂工程问题，并提出解决该类问题的解决方法等。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">4、课程体系</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">高级语言程序设计、电路基础、模拟电子技术、数字电路与逻辑设计、电子工艺实训、电子技术课程设计、信号与系统、微机原理与接口技术、数字信号处理、单片机原理及应用、自动控制原理、嵌入式系统及应用、电子线路综合设计与实训、电气与可编程控制系统实训等。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">5、师资队伍</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">结合IT产业发展特点，以人才需求为导向进行师资队伍建设。本专业从国内外高校引进学科带头人与具有博士学位的高素质人才，从企业引进项目经验丰富的优秀工程师，鼓励在职在岗的硕士学历教师攻读博士学位，每年寒暑假派遣教师前往企业学习先进技术。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">6、教学条件</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">学院现有总价值千万元的各类GPU等服务器15台，设有电子工艺实验室、电子综合设计实验室、虚拟电子技术基础实验室、机器视觉创新实验室、嵌入式应用开放实验室等17个主要用于科学研究、实验教学、学科竞赛、实习实训。学院与广州粤嵌、广州联通、深圳信盈达等企业合作建设了3个创新实验室，用于共建课程、学生实习实训、教师技术培训等。设有一个省级重点科研平台（多媒体内容安全技术研究中心），两个校级科研平台（大数据与人工智能创新研究中心、多媒体大数据安全技术研究中心），科学研究空间面积达到1000平米，可满足研究人员和教师科学研究的需要。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">7、专业特色</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本专业注重校企合作，与广州联通、广州粤嵌、深圳信盈达共建特色班，提升校企融合的深度，在工程认证思想的指导下，提高学生实践创新、工程技术能力，培养应用型工程人才。本专业2013年获批广东省战略新兴产业特色专业，2017年获批电子信息工程人才培养教学团队，2019年获批广东省重点专业，2021年度获批为广东省一流专业建设点，2021年被艾瑞深校友会网评选为6星级专业。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="text-indent:2em;font-size:20px"><br></span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="text-indent:2em;font-size:20px">二、通信工程专业介绍</span><br></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">1、专业定位</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">通信工程自2010年成立以来，专业遵循 “厚基础、宽口径、重实践、强能力”的教学指导思想，坚持“紧贴信息技术与市场需求”的办学理念，以就业为导向，强调专业课程体系与社会需求的高度接轨，培养适应新兴电子通信产业，掌握信息与通信系统理论与设计相关专业理论知识，德智体美全面发展，具有较高文化素质修养、敬业精神和社会责任感，具有良好学习能力、工程实践能力、协同合作能力和创新创业能力的应用型高素质人才。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">2、培养目标</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本专业以工程教育专业认证为标准，在立德树人的指导思想下，培养具有爱国主义精神和社会责任感，掌握自然科学基础知识和信息与通信系统的基础理论与分析设计方法，能在信息处理、通信系统、电子工程、计算机通信网络及其它相关领域的企事业单位，从事信息与通信工程项目的设计开发、生产运行、设备维护和经营管理等方面的工作。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">3、培养规格</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">学校明确本专业的学制为4年。按照学分制管理机制，实行弹性学习年限，但不得低于3年或超过7年。最低毕业总学分为156+（8~10）（8~10学分是学生成长教育课程学分）。要求学生能掌握电子信息与通信系统等方面的基本理论、基本技能与分析方法，系统掌握通信电路、传输理论、信号处理、通信网络、通信技术等专业基础理论，运用扎实的通信工程专业基础知识，结合通信技术前沿的研究课题及发展趋势，基于科学原理并采用科学方法对工程问题进行研究，初步具备独立实验、设计、分析和调试的能力，并能够在问题求解环节中体现一定的创新意识，考虑社会、健康、安全、法律、文化以及环境等因素。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">4、课程体系</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">专业课程设置全面、注重实践，可覆盖通信基础、无线通信、射频通信、移动通信、互联网通信、光通信、电磁理论、现代交换技术等领域的专业知识。具体课程包括高级语言程序设计，电路基础，模拟电子技术，数字电路与逻辑，信号与系统、通信网基础，通信原理，通信系统实验，通信电子线路，移动通信技术，移动通信网络规划与优化，计算机网络及实训，嵌入式系统与应用、光纤通信等。本专业与13家企业开展深度的校企合作模式，将企业资源引入课堂，在实训课程、毕业论文、项目竞赛等开放型课程活动中增设企业参与的定制课程。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">5、师资队伍</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本专业拥有一支职称结构合理、学历层次高、教学与实践经验丰富、师风良好的高水平师资队伍，既有海内外名校毕业的高学历人才，又有知名企业从业过的工程型人才，团队教师的研究方向涉及通信行业诸多领域，在师资结构上做到研究型和实践性相结合、学术型和授课型相结合，其专业素养为培养应用型通信人才提供了坚实的保证。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">6、教学条件</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">学院现有总价值千万元的各类GPU等服务器15台，设有通信原理与系统实验室、通信综合实验室、电路与模拟电子技术实验室、计算机网络实训实验室、嵌入式/物联网实验室、机器视觉创新实验室等17个可用于科学研究、实验教学、学科竞赛的实践教室。学院与广州粤嵌、中国联通、深圳信盈达等企业合作建设了3个创新实验室，用于共建课程、学生实习实训、教师技术培训等。设有一个省级重点科研平台（多媒体内容安全技术研究中心），两个校级科研平台（大数据与人工智能创新研究中心、多媒体大数据安全技术研究中心），科学研究空间面积达到1000平米，可满足研究人员和教师科学研究的需要。</span></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><strong><span style="font-size:18px">7、专业特色</span></strong></p> 
    <p style="line-height: 180%; text-indent: 2em; font-size: 14pt;text-align: left;"><span style="font-size:18px">本专业是艾瑞深校友会网评选4星级专业、校内新工科重点建设专业，是跨越通信、电子、计算机等学科的宽口径专业，因此在人才培养方面注重学科之间的交叉融合。通过与广州联通等企业建立深度合作的方式，培养集电子技术、通信技术、计算机网络技术为一身的5G时代高素质人才。</span></p>
