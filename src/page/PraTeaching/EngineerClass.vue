      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
卓越工程师特色班全称为：珠江卓越工程师人才培养特色班，该特色班建设以振兴学院发展、深化学院人才培养模式改革为目标，以服务珠三角地区社会经济发展 为基础，充分利用广东省电子信息技术产业优势，以市场人才需求为导向，联合企业，开展校企合作人才培养，旨在培养具有南方学院特色的电子通信、电气、软件类卓越工程师人才。具体内容如下：
      </p>
      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
1、成立“珠江卓越工程师人才培养特色班专业指导小组”：    专业指导小组与班主任及本科生导师不同。业指导小组由高校教师和企业工程技术人员共同组成，而且
            来自企业的工程技术人员必须达到30%。让来自企业的工程技术人员充分参与学生在校学习阶段的指导与培养。借此，企业的工程技术人员可更加深入地了解学  生、了解课堂、了解教学，有目的地进行培养。专业指导小组的工作也将延伸至学生在企业的工程训练，从而也为高校教师深入了解生产、了解企业需求、了解产业提供的条件。
      </p>

      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
2、学生选拔与分组：从我系每个专业各选拔出约三分之一的学生，组成卓越电子工程师（60人）和卓越软件工程师（60人）的人才培养特色班。学生根据兴趣与特长以3-4人为团队成立项目学习兴趣小组。
      </p>

      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
3、校企合作建设：在加强原有工程实践教学基地建设的基础上，依据行业对专业人才培养的要求，充分利用行业优势和资源，同电子电气通信、软件优秀企业建立校企联合培养体，采用双导师团队制度，对于每个项目组，学校和企业分别配备富有工程实践经验的优秀教师、工程技术人员指导。建立健全学生企业学习期间多种保障措施。
      </p>

      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
4、培养方案：卓越工程师的培养分电子工程师和软件工程师，培养体系有四大类：基础理论课程；工程技术基础课程；卓越工程师素质课程；卓越工程师方向课程。
      </p>

      <p style="text-indent: 43px; line-height: 37px;text-align: left;">5、培养特点：一是行业企业深度参与培养的全过程，二是按通用标准和行业标准培养工程技术人才，三是强化培养学生的工程能力和创新能力。
      </p>
      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
6、实施原则：遵循教育部“行业指导、校企合作、分类实施、形式多样”的原则，从而创立高校与行业企业联合培养人才的新机制、创新工程教育的人才培养机制、建设高水平工程教育师资队伍、扩大工程教育的对外开放、制订卓越计划人才培养标准等五方面推进该计划的实施。
      </p>
      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
7、证书：毕业时符合学院相关条件的发所学专业“毕业证”、“学位证”以及中山大学南方学院“珠江卓越工程师人才培养特色班”荣誉证书。
      </p>
      <p style="text-indent: 43px; line-height: 37px;text-align: left;">
8、成果：目前已开设软件卓越工程师班、电子与通信卓越工程师班。经过几年的培养，已有两届“卓越工程师班”的学生完成在校学习，并顺利结业。创建至今，卓越工程师班学子获得国家级大学生创新创业训练项目5项，“粤港信息科技青年创业计划”CCMF基金资助10万元港币，广东省大学生科技创新重点培育项目1项，在学科竞赛中获奖150多项，其中省级以上奖项77项。
      </p>

